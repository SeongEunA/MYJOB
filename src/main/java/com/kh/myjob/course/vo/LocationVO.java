package com.kh.myjob.course.vo;

public class LocationVO {
	private String highLocationName;
	private String lowLocationName;
	private String locationTempCode;
	private String locationLandCode;
	
	public String getHighLocationName() {
		return highLocationName;
	}
	public void setHighLocationName(String highLocationName) {
		this.highLocationName = highLocationName;
	}
	public String getLowLocationName() {
		return lowLocationName;
	}
	public void setLowLocationName(String lowLocationName) {
		this.lowLocationName = lowLocationName;
	}
	public String getLocationTempCode() {
		return locationTempCode;
	}
	public void setLocationTempCode(String locationTempCode) {
		this.locationTempCode = locationTempCode;
	}
	public String getLocationLandCode() {
		return locationLandCode;
	}
	public void setLocationLandCode(String locationLandCode) {
		this.locationLandCode = locationLandCode;
	}
}
