public class ReviewImgVO {
	private String reviewImgCode;
	private String reviewImgOriginName;
	private String reviewImgAttachedName;
	private String reviewBoardCode;
	private String isMain;
	
	public String getReviewImgCode() {
		return reviewImgCode;
	}
	public void setReviewImgCode(String reviewImgCode) {
		this.reviewImgCode = reviewImgCode;
	}
	public String getReviewImgOriginName() {
		return reviewImgOriginName;
	}
	public void setReviewImgOriginName(String reviewImgOriginName) {
		this.reviewImgOriginName = reviewImgOriginName;
	}
	public String getReviewImgAttachedName() {
		return reviewImgAttachedName;
	}
	public void setReviewImgAttachedName(String reviewImgAttachedName) {
		this.reviewImgAttachedName = reviewImgAttachedName;
	}
	public String getReviewBoardCode() {
		return reviewBoardCode;
	}
	public void setReviewBoardCode(String reviewBoardCode) {
		this.reviewBoardCode = reviewBoardCode;
	}
	public String getIsMain() {
		return isMain;
	}
	public void setIsMain(String isMain) {
		this.isMain = isMain;
	}
	
	
	
	
}
