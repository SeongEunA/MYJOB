package com.kh.myjob.review.vo;

public class ReviewRecomVO {
	private String reviewRecommendCode;
	private String memberId;
	private String reviewBoardCode;
	private String isRecommend;
	
	public String getReviewRecommendCode() {
		return reviewRecommendCode;
	}
	public void setReviewRecommendCode(String reviewRecommendCode) {
		this.reviewRecommendCode = reviewRecommendCode;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getReviewBoardCode() {
		return reviewBoardCode;
	}
	public void setReviewBoardCode(String reviewBoardCode) {
		this.reviewBoardCode = reviewBoardCode;
	}
	public String getIsRecommend() {
		return isRecommend;
	}
	public void setIsRecommend(String isRecommend) {
		this.isRecommend = isRecommend;
	}
	
	
}
